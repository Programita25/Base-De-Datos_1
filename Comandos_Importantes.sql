SELECT--> Extrae datos de la base de datos
UPDATE--> Actualiza datos en la base de datos 
DELETE--> Elimina datos de la base de datos
INSERT INTO--> Inserta nuevos datos en la base de datos
CREATE DATABASE --> Crea una nueva base de datos 
ALTER DATABASE--> Modifica una base de datos 
CREATE TABLE --> Crea una nueva Tabla 
ALTER TABLE --> Modifica una tabla 
DROP TABLE --> Elimina una tabla 
CREATE INDEX --> Creacion de una organizacion (indice)
DROP INDEX --> Elimina la organizacion (indice)